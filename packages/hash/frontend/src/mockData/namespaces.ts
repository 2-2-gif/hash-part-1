import { useMutation } from "@apollo/client";

import { CreateUserMutation, CreateUserMutationVariables } from "../graphql/autoGeneratedTypes";
import { createUser } from "../graphql/queries/user.queries";

export const createNamespaces = async () => {

  const [createUserFn, data] = useMutation(createUser);

  const users: CreateUserMutationVariables[] = [
    {
      email: "aj@hash.ai",
      shortname: "akash",
    }
  ];
  const res = await Promise.all(
    users.map(async user => await createUserFn({variables: user}))
  );

  console.log(res);
  console.log(data);
};

