import { useQuery } from "@apollo/client";
import { GetServerSideProps } from "next";
import Link from "next/link";

import {
  GetNamespacePagesQuery,
  GetNamespacePagesQueryVariables,
  GetNamespacesQuery,
} from "../graphql/autoGeneratedTypes";
import {
  getNamespacePages,
  getNamespaces,
} from "../graphql/queries/namespace.queries";

import styles from "./index.module.scss";
import { createApolloClient } from "../graphql/createApolloClient";

export const getServerSideProps: GetServerSideProps = async () => {
  const client = createApolloClient();

  const result = await client.query<GetNamespacesQuery>({
    query: getNamespaces,
  });

  if (result) {
    const { namespaces } = result.data;

    const firstPage = await namespaces.reduce(
      (promise, namespace) =>
        promise.then(async (page) => {
          if (!page) {
            const result = await client.query<
              GetNamespacePagesQuery,
              GetNamespacePagesQueryVariables
            >({
              query: getNamespacePages,
              variables: {
                namespaceId: namespace.namespaceId,
              },
            });

            const pages = result.data.namespacePages;
            if (pages.length > 0) {
              return `${namespace.namespaceId}/${pages[0].id}`;
            }
          }

          return page;
        }),
      Promise.resolve<null | string>(null)
    );

    if (firstPage) {
      return {
        redirect: {
          destination: firstPage,
          permanent: false,
        },
      };
    }
  }

  return { props: {} };
};

export default function Home() {
  const { data } = useQuery<GetNamespacesQuery>(getNamespaces);

  return (
    <main className={styles.Main}>
      <header>
        <h1>HASH.dev</h1>
      </header>

      <section>
        <h2>Namespaces in this instance</h2>
        <ul>
          {data?.namespaces.map((namespace) => (
            <li key={namespace.id}>
              <Link href={`/namespace/${namespace.id}`}>
                <a>{namespace.properties.shortname}</a>
              </Link>
            </li>
          ))}
        </ul>
      </section>

      <section>
        <h2>Block playground</h2>
        <p>
          <Link href="/playground">
            <a>Click here to visit the block playground</a>
          </Link>
        </p>
      </section>
    </main>
  );
}
