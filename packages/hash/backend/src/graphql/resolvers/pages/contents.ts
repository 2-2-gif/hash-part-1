import { Resolver } from "../../autoGeneratedTypes";
import { entities } from "../../../mockData/entities";
import { DbBlock, DbPage } from "../../../types/dbTypes";

export const contents: Resolver<Promise<DbBlock[]>, DbPage["properties"]> =
  async ({ contents }) => {
    return Promise.all(
      contents.map(
        (item: any) =>
          entities.find((entity) => entity.id === item.entityId) as DbBlock
      )
    );
  };
